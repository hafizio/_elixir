defmodule FloodFillTest do
  use ExUnit.Case, async: true

  test "call([args])" do
    area = [
      ["#", "#", "#", "#", "#", "#", "#", "#", "#", "#"],
      ["#", "#", "#", "#", "#", "#", "#", "#", "#", "#"],
      ["#", "#", "#", "#", "#", "#", "#", "#", "#", "#"],
      ["#", "#", "#", "-", "#", "#", "#", "#", "#", "#"],
      ["#", "#", "#", "-", "-", "#", "#", "#", "#", "#"],
      ["#", "#", "#", "-", "-", "#", "#", "#", "#", "#"],
      ["#", "#", "#", "-", "-", "#", "#", "#", "#", "#"],
      ["#", "#", "#", "-", "-", "#", "#", "#", "#", "#"],
      ["#", "#", "#", "#", "#", "#", "#", "#", "#", "#"],
      ["#", "#", "#", "#", "#", "#", "#", "#", "#", "#"]
    ]

    flood = [
      ["#", "#", "#", "#", "#", "#", "#", "#", "#", "#"],
      ["#", "#", "#", "#", "#", "#", "#", "#", "#", "#"],
      ["#", "#", "#", "#", "#", "#", "#", "#", "#", "#"],
      ["#", "#", "#", "█", "#", "#", "#", "#", "#", "#"],
      ["#", "#", "#", "█", "█", "#", "#", "#", "#", "#"],
      ["#", "#", "#", "█", "█", "#", "#", "#", "#", "#"],
      ["#", "#", "#", "█", "█", "#", "#", "#", "#", "#"],
      ["#", "#", "#", "█", "█", "#", "#", "#", "#", "#"],
      ["#", "#", "#", "#", "#", "#", "#", "#", "#", "#"],
      ["#", "#", "#", "#", "#", "#", "#", "#", "#", "#"]
    ]

    assert FloodFill.call(area) == flood
  end

end
